package org.fasttrackit;

public class Animal {
    String name;
    int health;
    int hungry;
    int spirit;
    String favfood;
    String favactivity;

    public Animal(String name, int health, int hungry, int spirit, String favfood, String favactivity) {
        this.name = name;
        this.health = health;
        this.hungry = hungry;
        this.spirit = spirit;
        this.favfood = favfood;
        this.favactivity = favactivity;
    }
}
